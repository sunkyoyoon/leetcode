# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def rightSideView(self, root: Optional[TreeNode]) -> List[int]:
        ans = [] 
        queue = deque() 
        if root:
            queue.append(root)
        else:
            return []

        while queue:
            length = len(queue)
            for i in range(length):
                node = queue.popleft() 
                if node.left:
                    queue.append(node.left)
                if node.right:
                    queue.append(node.right)
                if i == length - 1:
                    ans.append(node.val)
        
        return ans
          
